                        <p>市人口和计划生育局党总支下设2个党支部：人口和计划生育局党支部，计划生育服务站党支部。</p>
                        <p>市人口和计划生育局内设5个机构：办公室、财会股、宣传教育科技股、规划统计股、政策法规股。</p>
                        <p>二、主要职责</p>
                        <p>1、贯彻执行国家、自治区、呼伦贝尔市关于人口和计划生育工作的方针、政策、法律和法规并监督实施，研究、制定扎兰屯市人口和计划生育工作的有关办法、措施并组织实施；负责协调推动有关部门、群众团体履行人口和计划生育工作相关职责，促进人口和计划生育方针政策在教育、卫生、文化、就业和社会保障等工作中的衔接配合，会同有关部门提出促进出生人口性别平衡的政策措施。</p>
