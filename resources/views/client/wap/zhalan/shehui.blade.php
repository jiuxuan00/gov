                        <p>五、工作情况</p>
                        <p>近年来，扎兰屯市的人口和计划生育工作始终处在呼伦贝尔市优秀行列。2010年，扎兰屯市被命名为自治区级计划生育优质服务先进市。全市现有“全国人口和计划生育依法行政示范乡镇（街道）”1个，“全国人口和计划生育基层群众自治示范村”1个，呼伦贝尔市级计划生育优质服务先进乡镇4个，依法行政示范乡镇2个。经过30多年的发展，人口再生产类型已实现转变，进入了低生育水平稳定期。1983年到2012年，扎兰屯市人口出生率从13.29‰下降到9.14‰，自然增长率由9.01‰降至4.65‰。人口出生率连续21年控制在10‰以下。计划生育率保持较高水平。2012年，全市的符合政策生育率为94%。与1983年69.22%相比，上升24.78个百分点，连续26年保持在90%以上。计划生育工作方法由简单、粗暴执法转变为统筹解决人口问题；由以往的以“罚”为主转变为以“奖”为主，突出了以人为本，构建了以“一奖”、“两爱”、“三助”、“四优”为特点，“十有政策”（政策生育有奖励、优生优育有偏爱、孩子上学有关爱、患病就医有资助、计生手术有补助、日常生活有帮助、特殊家庭有优扶、发展生产有优先、老年生活有优待、计生干部有优助）为主要内容的计划生育利益导向政策新体系，累计惠及30多万人次。群众实行计划生育自觉性显著增强。2012年，全市独生子女领证率为19.11%，与有记录的最早年代1983年的10.30%相比，提高了8.81个百分点。农村符合条件自愿放弃再生育家庭由2004年的260户增加到2552户，增长了8.8倍。至2013年3月31日统计，全市14周岁以下的独生子女领证家庭为13014户。</p>
