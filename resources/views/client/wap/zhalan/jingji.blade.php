                        <p>2012年，扎兰屯市在市委、市政府的正确领导下，以科学发展观为指导，紧紧围绕“一个主题、两条主线、三个带动战略”，筑牢基础、壮大实力、改善民生，全市经济总量显著增强，产业结构不断调整，社会各项事业健康发展，为全面建成小康社会奠定了良好基础。</p>
                        <p>一、综合</p>
                        <p>2012年全市经济继续呈现平稳增长态势，全年地区生产总值完成1486485万元，按可比价格计算，比上年增长14.5%。其中：第一产业增加值完成385217万元，比上年增长6.2%；第二产业增加值完成770214万元，比上年增长24.1%；第三产业增加值完成331054万元，比上年增长4.2%。三次产业结构比由上年的27.5：48.1：24.4调整为25.9：51.8：22.3。人均地区生产总值为34993元，比上年增长18.5%。</p>
