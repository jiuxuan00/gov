                        <p>劳动就业不断扩大。2012年全市城镇新增就业7184人。城镇登记失业率3.88%。农村劳动力转移就业42087人。实现劳务经济收入74648万元。2012年全市农作物播种面积212787公顷，比上年下降3.6%。其中，粮食作物播种面积190650公顷，比上年增长0.5%。全年粮食产量20.56亿斤，比上年增长2.5%。在粮食作物中，玉米产量17.28亿斤，比上年增长10.6%；大豆产量0.93亿斤，比上年下降29.0%；马铃薯（折粮）产量1.29亿斤，比上年下降28.3%。在经济作物中，油料产量0.66亿斤，比上年下降7.6%。粮经饲种植比为89.6：8.5：1.9。</p>
